"""IQ file demodulator. Recovers bits from a .cf32 or .sigmf-data file
generated by iqgen (or compatible). Knows about all 10 modulations, all 5
pulse-shaping filters, and multi-frequency (concurrent or hopping).

This is a noise-free matched receiver — it knows the parameters (either
from SigMF metadata or supplied manually) and demodulates accordingly. It
is NOT a blind receiver; there's no carrier/timing recovery.
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass, field
from pathlib import Path
from typing import Optional

import numpy as np

from .config import BITS_PER_SYMBOL, VALID_FILTERS
from .filters import PulseShaper

log = logging.getLogger(__name__)


@dataclass
class ReceiveParams:
    """Everything the demodulator needs. Populated either from SigMF
    metadata or directly by the caller."""

    modulation: str
    sample_rate: float
    bitrate: float
    filter_type: str = "none"
    span_symbols: int = 10
    roll_off: float = 0.35
    bt_product: float = 0.35
    gray_coding: bool = True
    initial_phase: float = 0.0

    # multi-freq (defaults match generator: single carrier at offset 0)
    channel_mode: str = "concurrent"
    channel_offsets_hz: list = field(default_factory=lambda: [0.0])
    hop_duration_sec: Optional[float] = None

    @property
    def bits_per_symbol(self) -> int:
        return BITS_PER_SYMBOL[self.modulation]

    @property
    def symbol_rate(self) -> float:
        return self.bitrate / self.bits_per_symbol

    @property
    def samples_per_symbol(self) -> int:
        return int(round(self.sample_rate / self.symbol_rate))


