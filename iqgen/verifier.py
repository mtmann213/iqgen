@dataclass
class VerifyReport:
    n_recovered_bits: int
    n_expected_bits: int
    n_compared: int
    n_errors: int

    @property
    def ber(self) -> float:
        return self.n_errors / self.n_compared if self.n_compared else float("nan")

    def __str__(self) -> str:
        if self.n_expected_bits == 0:
            return (f"Recovered {self.n_recovered_bits} bits "
                    f"(no expected bits supplied; no BER computed)")
        return (f"Recovered {self.n_recovered_bits} bits, "
                f"expected {self.n_expected_bits}, "
                f"compared {self.n_compared}, "
                f"errors {self.n_errors}, "
                f"BER {self.ber:.3e}")


def compare_bits(recovered: np.ndarray, expected: np.ndarray) -> VerifyReport:
    n = min(recovered.size, expected.size)
    if n == 0:
        return VerifyReport(recovered.size, expected.size, 0, 0)
    errs = int(np.sum(recovered[:n] != expected[:n]))
    return VerifyReport(recovered.size, expected.size, n, errs)


def parse_bits(s: str) -> np.ndarray:
    """Accept '0'/'1' characters; strip everything else."""
    arr = np.array([int(c) for c in s if c in "01"], dtype=np.uint8)
    return arr


